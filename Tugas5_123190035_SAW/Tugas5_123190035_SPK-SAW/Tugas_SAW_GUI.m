for j=1:n,
    if (data2(1,j)==1), %statement untuk Atribut Benefit
        R(:,j)=data1(:,j)./max(data1(:,j));
    else
        R(:,j)=min(data1(:,j))./data1(:,j); %Atribut Cost
    end;
    set(handles.uitable10,'Data',R);  
