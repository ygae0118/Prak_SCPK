for i=1:m,
 V(i)= sum(data3.*R(i,:));
 set(handles.uitable11,'Data',V);
  [nilai,urutan]=max(V);
set(handles.text40,'string',(nilai));
if(urutan == 1)
   set(handles.nama,'string',(nama1));
elseif (urutan == 2)
   set(handles.nama,'string',(nama2));
elseif (urutan == 3)
   set(handles.nama,'string',(nama3));
elseif (urutan == 4)
   set(handles.nama,'string',(nama4)); 
elseif (urutan == 5)
   set(handles.nama,'string',(nama5));
elseif (urutan == 6)
   set(handles.nama,'string',(nama6));   
end;

end;

